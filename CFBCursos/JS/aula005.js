// Operadores relacionais em JavaScript

let x = 10;
let y = 20;

// Igual a
console.log(x == y);   // false (x não é igual a y)

// Estritamente igual a (compara valor e tipo)
console.log(x === y);  // false

// Diferente de
console.log(x != y);   // true (x é diferente de y)

// Estritamente diferente de (valor e tipo)
console.log(x !== y);  // true

// Maior que
console.log(x > y);    // false

// Maior ou igual a
console.log(x >= y);   // false

// Menor que
console.log(x < y);    // true

// Menor ou igual a
console.log(x <= y);   // true
