// OPERADORES ARITMETICOS

// Operadores aritméticos em JavaScript

let a = 10;
let b = 3;

// Soma
console.log(a + b);  // Resultado: 13

// Subtração
console.log(a - b);  // Resultado: 7

// Multiplicação
console.log(a * b);  // Resultado: 30

// Divisão
console.log(a / b);  // Resultado: 3.3333333333333335

// Módulo (resto da divisão)
console.log(a % b);  // Resultado: 1

// Potenciação (a elevado a b)
console.log(a ** b); // Resultado: 1000
